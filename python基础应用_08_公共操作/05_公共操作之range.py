for i in range(1, 10, 1):
    print(i, end=' ')
print()
for i in range(1, 10):
    print(i, end=' ')
print()
for i in range(1, 10, 2):
    print(i, end=' ')
print()
for i in range(10):
    print(i, end=' ')
print()
for i in range(10, 0, -1):
    print(i, end=' ')

